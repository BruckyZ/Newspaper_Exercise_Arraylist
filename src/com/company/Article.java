package com.company;

public class Article
{
	public String getTittle()
	{
		return Tittle;
	}

	public void setTittle(String tittle)
	{
		Tittle = tittle;
	}

	public String getDate()
	{
		return Date;
	}

	public void setDate(String date)
	{
		Date = date;
	}

	private String Tittle;
	private String Date;

}
